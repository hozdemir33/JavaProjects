package com.company;

public class loop_7 {

    public static void main(String[] args) {

        //We can use StringBuffer to REVERSE ANY STRING VALUES;

        StringBuffer name2=new StringBuffer("Ali Can");
        name2.reverse();
        System.out.println(name2);

        //We can also use StringBuilder to REVERSE ANY STRING VALUES;

        StringBuilder name3=new StringBuilder("YUNUF");
        name3.reverse();
        System.out.println(name3);

   }
}